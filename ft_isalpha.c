#include "cub3d.h"

int	ft_isalpha(char *ch)
{
	int	i;

	i = 0;
	while (ch[i])
	{
		if ((ch[i] >= 'a' && ch[i] <= 'z') || (ch[i] >= 'A' && ch[i] <= 'Z'))
			return (1);
		i++;
	}
	return (0);
}
