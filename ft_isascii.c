#include "cub3d.h"

int	ft_isascii(char *a)
{
	int	i;

	i = 0;
	while (a[i])
	{
		if (a[i] == '-' || a[i] == '+')
			i++;
		if (!(a[i] >= 48 && a[i] <= 57))
			return (1);
		i++;
	}
	return (0);
}
